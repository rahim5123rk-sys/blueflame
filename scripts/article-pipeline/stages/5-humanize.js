/**
 * STAGE 5 — HUMANIZE
 *
 * Transforms the AI-generated content into authentic, natural-sounding prose.
 *
 * What this stage eliminates:
 *  - Repetitive sentence structures ("It's important to...", "It's worth noting...")
 *  - Over-use of lists where prose would flow better
 *  - AI giveaways: "delve into", "In conclusion", "Furthermore", "Moreover"
 *  - Passive voice overuse
 *  - Unnatural keyword stuffing
 *
 * What this stage adds:
 *  - Real engineer personality and voice
 *  - Conversational asides and natural transitions
 *  - Appropriate informal contractions (you'll, it's, don't, we're)
 *  - British idioms and turns of phrase
 *  - Anecdotes or realistic scenarios ("We recently visited a house in Droitwich where...")
 *  - Varied sentence rhythm (short punchy sentences mixed with longer ones)
 */

import { streamOpus } from '../utils/claude.js';

const SYSTEM = `You are a Gas Safe registered heating engineer from Worcester with 15 years of experience. You've decided to write blog articles for your company (Blue Flame Gas Services) in your own words.

Your writing voice is:
- Direct and practical ("Here's what actually happens...")
- Honest and slightly self-deprecating ("I'll be straight with you...")
- Expert but never condescending ("You don't need to know the technical details, but...")
- British — you use words like "bloke", "properly", "sorted", "bodge", "faff", "knackered"
- Occasionally first-person ("We see this every winter...", "In my experience...")
- Confident about your trade knowledge
- Mildly sceptical of overclaiming ("Some engineers will say X, but in reality...")

You are OBSESSED with removing:
- "delve into", "dive into", "explore", "Furthermore", "Moreover", "Additionally"
- "It is important to note", "It is worth noting", "Notably"
- "In conclusion", "To summarize", "In summary"
- Starting sentences with "There are" or "There is"
- Starting every bullet point with the same word structure
- Excessive passive voice
- Any word or phrase that sounds like it was written by a robot`;

export async function runHumanize(state) {
  const { plan, written } = state;

  console.log('\n╔════════════════════════════════════════╗');
  console.log('║  STAGE 5 — HUMANIZE                   ║');
  console.log('╚════════════════════════════════════════╝');

  // Humanize the intro
  console.log('  Humanizing intro...');
  const introResult = await streamOpus(
    [
      {
        role: 'user',
        content: `Rewrite this article intro to sound like it was written by a real heating engineer from Worcester — not an AI.

CURRENT INTRO:
${written.intro}

RULES:
- Keep all the factual information
- Make it sound genuinely conversational and human
- Vary sentence length — some short, some longer
- You can start with a short punchy sentence (1 fragment is OK)
- Remove any AI-sounding phrases
- Keep it under 120 words
- UK English only

Return ONLY the rewritten intro paragraph, nothing else.`,
      },
    ],
    { system: SYSTEM, maxTokens: 400, thinking: false },
  );

  const humanIntro = introResult.text.trim();

  // Humanize each section
  const humanSections = [];
  for (let i = 0; i < written.sections.length; i++) {
    const section = written.sections[i];
    console.log(`  Humanizing section ${i + 1}/${written.sections.length}: "${section.h2}"...`);

    const prompt = `Rewrite this article section content to sound completely human and natural — like it's written by a working Gas Safe engineer in Worcester, not generated by AI.

SECTION HEADING: "${section.h2}"

MAIN CONTENT TO REWRITE:
${section.content}

${section.subsections?.length ? `SUBSECTION CONTENT TO REWRITE:\n${section.subsections.map((s) => `[${s.h3}]\n${s.content}`).join('\n\n')}` : ''}

RULES:
1. Keep ALL factual content, statistics, and prices — just change the writing style
2. Vary sentence structure dramatically — short punchy sentences, longer explanatory ones
3. Add ONE natural anecdote or real-world scenario ("We regularly visit homes in Worcestershire where...")
4. Use contractions freely (you'll, it's, don't, we've, that's)
5. Remove ANY phrase that sounds AI-generated
6. If there's a checklist or table in the section, DO NOT change those — only rewrite prose
7. UK English throughout
8. Keep it the same length — don't add padding, don't cut important content

Return a JSON object:
{
  "h2": "${section.h2}",
  "content": "Rewritten main content",
  "subsections": [
    { "h3": "heading", "content": "Rewritten subsection content", "checklist": [...] }
  ]
}

Include the same structure (table, callout, warning, checklist, externalLinks) from the original — just rewrite the prose content fields.
If the original had: table, callout, warning, checklist, externalLinks — copy those across unchanged. Only rewrite "content" fields.`;

    const result = await streamOpus(
      [{ role: 'user', content: prompt }],
      { system: SYSTEM, maxTokens: 3000, thinking: false },
    );

    let humanSection;
    try {
      const jsonMatch = result.text.match(/\{[\s\S]*\}/);
      humanSection = jsonMatch ? JSON.parse(jsonMatch[0]) : section;

      // Preserve non-prose fields from the written stage
      humanSection = {
        ...section,
        content: humanSection.content || section.content,
        subsections: humanSection.subsections || section.subsections,
      };
    } catch {
      humanSection = section;
    }

    humanSections.push(humanSection);
  }

  // Humanize FAQs
  console.log('  Humanizing FAQ answers...');
  const faqResult = await streamOpus(
    [
      {
        role: 'user',
        content: `Rewrite these FAQ answers to sound like they're written by a real heating engineer from Worcester — direct, honest, and human. Not AI-generated.

Keep all factual content. Use contractions. Be conversational. No fluff. UK English.

Current FAQs:
${JSON.stringify(written.faqs, null, 2)}

Return as JSON array: [{ "q": "question", "a": "rewritten answer" }]`,
      },
    ],
    { system: SYSTEM, maxTokens: 3000, thinking: false },
  );

  let humanFaqs = written.faqs;
  try {
    const jsonMatch = faqResult.text.match(/\[[\s\S]*\]/);
    humanFaqs = jsonMatch ? JSON.parse(jsonMatch[0]) : written.faqs;
  } catch {
    humanFaqs = written.faqs;
  }

  const humanized = {
    intro: humanIntro,
    sections: humanSections,
    faqs: humanFaqs,
  };

  console.log(`  ✓ Humanize complete:`);
  console.log(`    - ${humanSections.length} sections humanized`);
  console.log(`    - ${humanFaqs.length} FAQs humanized`);

  return { ...state, humanized };
}
